public class Player {
	private int vida;
	private String nombre;
	private int pos_x;
	private int por_y; 
	
	public int getVida() {
		return vida;
	}
	public void setVida(int vida) {
		this.vida = vida;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getPor_y() {
		return por_y;
	}
	public void setPor_y(int por_y) {
		this.por_y = por_y;
	}
	public int getPos_x() {
		return pos_x;
	}
	public void setPos_x(int pos_x) {
		this.pos_x = pos_x;
	}

}


